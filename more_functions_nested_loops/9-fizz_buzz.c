#include "main.h"
#include <stdio.h>

/**
 * main - affiche les nombres de 1 Ã  100, fizz pour multiple de 3
 * Buzz pour multiple de 5 et FizzBuzz pour les 2 multiple
 * Return: 0 si code bon
 */
int main(void)
{
	int i;

	for (i = 1; i <= 100; i++)
	{
		if (i % 3 == 0 && i % 5 != 0)
		{
			printf(" Fizz");
		} else if (i % 5 == 0 && i % 3 != 0)
		{
			printf(" Buzz");
		} else if (i % 3 == 0 && i % 5 == 0)
		{
			printf(" FizzBuzz");
		} else if (i == 1)
		{
			printf("%d", i);
		} else
		{
			printf(" %d", i);
		}
	}
	printf("\n");

	return (0);
}
